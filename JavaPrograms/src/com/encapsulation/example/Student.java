package com.encapsulation.example;

public class Student {
	
	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	
	public void setName(String Newname) {
		name = Newname;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int Newage) {
		age = Newage;
	}
	

}
